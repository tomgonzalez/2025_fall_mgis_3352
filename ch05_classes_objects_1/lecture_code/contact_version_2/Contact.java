public class Contact
{
   // Fields.  Note that access is now private.
   private String name;
   private String number;
   
   // Methods
   public String getName()
   {
      return name;
   }
   
   public void setName(String name)
   {
      this.name = name;
   }
   
   public String getNumber()
   {
      return number;
   }
   
   public void setNumber(String number)
   {
      this.number = number;
   }  
}