public class DivisionOperatorExamples
{
   public static void main(String[] args)
   {
      // Integer Division Examples
      // Notice the output is of type int.
      
      System.out.println(9/3);
      System.out.println(10/3);
      System.out.println(11/3);
      System.out.println(0/3);
      //System.out.println(3/0); // Will cause runtime error if uncommented.
      
      System.out.println(); 
      
      // Double Division Examples
      // Notice the output is of type double.
      
      System.out.println(9.0/3.0);
      System.out.println(10.0/3.0);
      System.out.println(11.0/3.0);
      System.out.println(0.0/0.3);
      //System.out.println(3/0); // Will cause runtime error if uncommented.                             
   }
}