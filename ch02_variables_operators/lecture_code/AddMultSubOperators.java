public class AddMultSubOperators
{
   public static void main(String[] args)
   { 
      // int operands
      // Notice result is of type int.
       
      System.out.println(5 + 7);
      System.out.println(5-7);
      System.out.println(5*7);
      
      System.out.println(); 
      
      // double operands
      // Notice result is of type double.
      
      System.out.println(5.0 + 7.0);
      System.out.println(5.0 - 7.0);
      System.out.println(5.0 * 7.0);
      
      System.out.println();
      
      System.out.println(1.4 + 9.2);
      System.out.println(8.536 - 19.00023);
      System.out.println(142.398 * 0.00021);     
       
   }
}